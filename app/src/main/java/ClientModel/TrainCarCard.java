package ClientModel;

public class TrainCarCard
{
    private String color;

    public TrainCarCard(String color)
    {
        this.color = color;
    }

    public String getColor()
    {
        return color;
    }

    public void setColor(String color)
    {
        this.color = color;
    }

    public String toString()
    {
        return color;
    }
}
